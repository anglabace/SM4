public interface CharConstant {

    /**
     * Carriage Return.
     */
    char CR = '\r';

    /**
     * Double Quote.
     */
    char DQUOTE = '\"';

    /**
     * Equals '='.
     */
    char EQ = '=';

    /**
     * Line Feed.
     */
    char LF = '\n';

    /**
     * Single Quote ['].
     */
    char QUOTE = '\'';

    /**
     * Space.
     */
    char SPACE = ' ';

    /**
     * Tab.
     */
    char TAB = '\t';

    /**
     * DOT.
     */
    char DOT = ',';

    /**
     * colon
     */
    char COLON = ':';
}
